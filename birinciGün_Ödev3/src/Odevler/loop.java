package demoBuyukSayýBulma;

public class loop {

	public static void main(String[] args) {

		// for
		for (int i = 1; i < 10; i++) {

			System.out.println(i);
		}
		System.out.println("FOR döngi bitti");
// while
		int i = 1;

		while (i < 20) {
			i++;
			System.out.println(i);

		}
		System.out.println("while döngüsü bitti");

		// do-while

		do {
			System.out.println(i);
			i++;

		} while (i < 30);
		System.out.println("do while bitti");

	}

}
